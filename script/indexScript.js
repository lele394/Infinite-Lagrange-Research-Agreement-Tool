function CreateShipFromLineArray(arr) {
    let ship = {
        //basics
        name : arr[0],
        type : arr[1],
        weight : parseInt(arr[2]),
        industry : GetIndustry(arr),
        //strategic filters
        outstandingFP : convertTF(arr[7]),
        sustained : convertTF(arr[8]),
        support : convertTF(arr[9]),
        aircraft : convertTF(arr[10]),
        //tactical
        projectile : convertTF(arr[11]),
        directFire : convertTF(arr[12]),
    }
    return ship
}


function convertTF(value){
    if(value == "TRUE"){return true}
    if(value == "FALSE"){return false}
}


